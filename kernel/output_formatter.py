#!/usr/bin/env python3
"""
Agency OS v5.0 — Output Formatter

Transforms raw AI text output into structured, professional deliverables:
- Per-studio output schemas
- Template-based report rendering
- Action item extraction
- Format conversion (MD → HTML, JSON → CSV)
"""

from __future__ import annotations

import csv
import io
import json
import logging
import re
from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Any

logger = logging.getLogger("agency.output")


@dataclass
class FormattedOutput:
    """Structured, formatted output from a studio."""

    raw: str = ""
    formatted: str = ""
    format_type: str = "markdown"  # markdown | html | json | csv
    action_items: list[dict[str, str]] = field(default_factory=list)
    sections: list[dict[str, str]] = field(default_factory=list)
    metadata: dict[str, Any] = field(default_factory=dict)
    studio: str = ""
    quality_notes: list[str] = field(default_factory=list)


